---@meta

---@class double:number, integer @A double-precision floating-point number. This is the same data type as all Lua numbers use.
---@class float:number, integer @A floating-point number. This is a single-precision floating point number. Whilst Lua only uses double-precision numbers, when a function takes a float, the game engine will immediately convert the double-precision number to single-precision.
---@class int:number, integer @32-bit signed integer. Possible values are -2,147,483,648 to 2,147,483,647.
---@class int8:number, integer @8-bit signed integer. Possible values are -128 to 127.
---@class uint:number, integer @32-bit unsigned integer. Possible values are 0 to 4,294,967,295.
---@class uint16:number, integer @16-bit unsigned integer. Possible values are 0 to 65535.
---@class uint64:number, integer @64-bit unsigned integer. Possible values are 0 to 18,446,744,073,709,551,615.
---@class uint8:number, integer @8-bit unsigned integer. Possible values are 0 to 255.
